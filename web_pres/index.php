<h3>Introduction:</h3>
This site contains a short tutorial on generic constellation design suited for wireless butterfly
network. It uses Python code to demonstrate the discussed algorithms. Since constellation design is
in fact a geometry task, a graphical demonstration is widely used. The author aims to enable the
reader a quick inside into the topic and also the ability to touch the problem using the code.
Therefore some derivations and statements are quite simplified at expanse of precision. For rigor
derivations and statements, please check available literature on wireless network coding.
